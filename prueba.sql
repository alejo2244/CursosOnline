IF EXISTS (SELECT * FROM sys.objects WHERE type = 'P' AND name = 'SP_VerificarGenero')
DROP PROCEDURE SP_VerificarGenero
GO
CREATE PROC SP_VerificarGenero AS
BEGIN
	if ((select COUNT(*) from TblUsuario) > 0)
	BEGIN
	select COUNT(*) CANTIDAD,
	(CASE WHEN genero = 0 THEN 'MASCULINO' 
		ELSE (CASE WHEN genero = 1 THEN 'FEMENINO' ELSE 'OTRO' END)END) AS [GENERO]
	from TblUsuario group by genero
	END
	ELSE
	BEGIN
		select 0, ''
	END
END

