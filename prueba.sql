IF EXISTS (SELECT * FROM sys.objects WHERE type = 'P' AND name = 'SP_VerificarEdades')
DROP PROCEDURE SP_VerificarEdades
GO
CREATE PROC SP_VerificarEdades AS
BEGIN
 Select ISNULL(SUM(A.PRIM), 0) as [Menos_10], ISNULL(SUM(A.SEG), 0) AS [11-20], ISNULL(SUM(A.TERC), 0) AS [21-50], ISNULL(SUM(A.CUAR), 0) AS [MAS_50] from
	(SELECT SUM(CASE WHEN Edad < 10 THEN 1 ELSE 0 END) AS PRIM,
        SUM(CASE WHEN Edad BETWEEN 11 AND 20 THEN 1 ELSE 0 END) AS SEG,
        SUM(CASE WHEN Edad BETWEEN 21 AND 50 THEN 1 ELSE 0 END) AS TERC,
		SUM(CASE WHEN Edad > 50 THEN 1 ELSE 0 END) AS CUAR
	from 
	(select (cast(datediff(dd,fechaNacimiento,GETDATE()) / 365.25 as int)) AS EDAD from TblUsuario) T
	group by EDAD) A
END

