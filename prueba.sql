	select USU.nombres + ' ' + USU.primerApellido + ' ' + USU.segundoApellido AS NOMBRE,
		PT.nombre AS PASATIEMPO from TblPasatiempos PT
	INNER JOIN TblPasatiemposUsuario PTUSU ON PTUSU.idPasatiempo = PT.id
	INNER JOIN TblUsuario USU ON USU.id = PTUSU.idUsuario
END
